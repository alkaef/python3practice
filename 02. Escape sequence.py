# \newline
print("line1 \
line2 \
line3")
# Results -> line1 line2 line3

# \\
print("\\")
# Results -> \

# \'
print('\'')
# RESULT -> '

# \"
print("\"")
#RESULT -> "

# \b
print("Hello \b World!")
#RESULT -> Hello  World!

# \f
print("Hello \f World!")
#RESULT -> Hello  World

# \n
print("Hello \n World!")
#RESULT -> Hello  World!

# \r
print("Hello \r World!")

# \t
print("Hello \t World!")

